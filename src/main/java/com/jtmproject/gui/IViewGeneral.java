package com.jtmproject.gui;

/**
 * This is an interface that the view of the configuration have to implements
 * @author Javier Tejedor
 */
public interface IViewGeneral {
	/**
	 * This is called when we want to charge the view
	 */
	public void chargeView();
	
	/**
	 * This is called when we want to remove the view
	 */
	public void removeElements();
	
	/**
	 * This is called when we want to save the data the view
	 */
	public void saveData();
	
	/**
	 * if the view can return any error, must be through this function
	 * @return
	 */
	public int errorCode();
}
