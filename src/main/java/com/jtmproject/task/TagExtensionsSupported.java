package com.jtmproject.task;

/**
 * This class stores an extension supported by the program
 * @author Javier Tejedor
 */
public class TagExtensionsSupported {

	private String extension;

	/**
	 * constructor
	 * @param extension
	 */
	public TagExtensionsSupported(String extension) {
		this.extension = extension;
	}

	/**
	 * returns the extension
	 * @return
	 */
	public String getExtension() {
		return extension;
	}
}
